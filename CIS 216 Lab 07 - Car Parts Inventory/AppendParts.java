package lab.pkg07;

public class AppendParts {
    
    private int newPartIDNumber;
    private String newPartDescription;
    private double newPartCost;
    private double newPartPrice;
    private double newPartWeight;
    private String newPartLocation;
    private int newPartQuantity;
    
    public void SetNewPartIDNumber(int anotherPartIDNumber) {
        
        newPartIDNumber = anotherPartIDNumber;
    
    }

    public void SetNewPartDescription(String anotherPartDescription) {
        
       newPartDescription = anotherPartDescription;
    
    }

    public void SetNewPartCost(double anotherPartCost) {
        
       newPartCost = anotherPartCost;
    
    }
    
    public void SetNewPartPrice(double anotherPartPrice) {
        
       newPartPrice = anotherPartPrice;
    
    }    

    public void SetNewPartWeight(double anotherPartWeight) {
        
        newPartWeight = anotherPartWeight;
    
    }

    public void SetNewPartLocation(String anotherPartLocation) {
        
        newPartLocation = anotherPartLocation;
    
    }

    public void SetNewPartQuantity(int anotherPartQuantity) {
        
        newPartQuantity = anotherPartQuantity;
    
    }
    
    public int GetNewPartIDNumber( ) {
        
        return newPartIDNumber;
        
    }
    
    public String GetNewPartDescription( ) {
        
        return newPartDescription;
                
    }
    
    public double GetNewPartCost( ) {
        
        return newPartCost;
        
    }
    
    public double GetNewPartPrice( ) {
        
        return newPartPrice;
        
    }
    
    public double GetNewPartWeight( ) {
        
        return newPartWeight;
                
    }
    
    public String GetNewPartLocation( ) {
        
        return newPartLocation;
        
    }
    
    public int GetNewPartQuantity( ) {
        
        return newPartQuantity;
                
    }
    
}
